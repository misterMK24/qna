feature 'User can delete an answer', %q{
  In order to delete an answer 
  for particular question
  As an authenticated user
  and author of this answer
  I'd like to be able to delete an answer
} do

  describe 'Authenticated user' do

    scenario 'author of answer tries to delete an answer'

    scenario 'third person tries to delete an answer'
  end

  scenario 'Unauthenticated user tries to delete an answer'
end
